package com.ctgu.lss.user.entity;

public class User {
	private String user_id;
	private String password;
	private String real_name;
	private int sex;
	private String reader_code;
	private String idcard_num;
	private String qq;
	private String dept;
	private String mobile;
	private String office_phone;
	private int user_type;
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getReal_name() {
		return real_name;
	}
	public void setReal_name(String real_name) {
		this.real_name = real_name;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	public String getReader_code() {
		return reader_code;
	}
	public void setReader_code(String reader_code) {
		this.reader_code = reader_code;
	}
	public String getIdcard_num() {
		return idcard_num;
	}
	public void setIdcard_num(String idcard_num) {
		this.idcard_num = idcard_num;
	}
	public String getQq() {
		return qq;
	}
	public void setQq(String qq) {
		this.qq = qq;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getOffice_phone() {
		return office_phone;
	}
	public void setOffice_phone(String office_phone) {
		this.office_phone = office_phone;
	}
	public int getUser_type() {
		return user_type;
	}
	public void setUser_type(int user_type) {
		this.user_type = user_type;
	}
	@Override
	public String toString() {
		return "User [user_id=" + user_id + ", password=" + password
				+ ", real_name=" + real_name + ", sex=" + sex
				+ ", reader_code=" + reader_code + ", idcard_num=" + idcard_num
				+ ", qq=" + qq + ", dept=" + dept + ", mobile=" + mobile
				+ ", office_phone=" + office_phone + ", user_type=" + user_type
				+ "]";
	}
	public User(String user_id, String password, String real_name, int sex,
			String reader_code, String idcard_num, String qq, String dept,
			String mobile, String office_phone, int user_type) {
		super();
		this.user_id = user_id;
		this.password = password;
		this.real_name = real_name;
		this.sex = sex;
		this.reader_code = reader_code;
		this.idcard_num = idcard_num;
		this.qq = qq;
		this.dept = dept;
		this.mobile = mobile;
		this.office_phone = office_phone;
		this.user_type = user_type;
	}
	public User() {
		super();
		// TODO Auto-generated constructor stub
	}
	
}
