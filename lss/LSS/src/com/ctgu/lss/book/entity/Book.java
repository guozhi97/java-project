package com.ctgu.lss.book.entity;

public class Book {
	private int book_id;
	private String order_id;
	private String index_num;
	private String book_name;
	private String author_name;
	private int book_status;
	public int getBook_id() {
		return book_id;
	}
	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	public String getOrder_id() {
		return order_id;
	}
	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}
	public String getIndex_num() {
		return index_num;
	}
	public void setIndex_num(String index_num) {
		this.index_num = index_num;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getAuthor_name() {
		return author_name;
	}
	public void setAuthor_name(String author_name) {
		this.author_name = author_name;
	}
	public int getBook_status() {
		return book_status;
	}
	public void setBook_status(int book_status) {
		this.book_status = book_status;
	}
	@Override
	public String toString() {
		return "Book [book_id=" + book_id + ", order_id=" + order_id
				+ ", index_num=" + index_num + ", book_name=" + book_name
				+ ", author_name=" + author_name + ", book_status="
				+ book_status + "]";
	}
	public Book(int book_id, String order_id, String index_num,
			String book_name, String author_name, int book_status) {
		super();
		this.book_id = book_id;
		this.order_id = order_id;
		this.index_num = index_num;
		this.book_name = book_name;
		this.author_name = author_name;
		this.book_status = book_status;
	}
	public Book() {
		super();
	}
}
