package com.ctgu.lss.common;

/**
 * 接收手机端发送过来的要发送短信的数据内容
 * 配合PackageData进行数据接收和处理
 * @author Administrator
 *
 */
public class MiniOrder {
	
	String package_code;
	String customer_phone;
	String customer_name;
	String customer_idcard;
	String area_num;
	
	public String getCustomer_phone() {
		return customer_phone;
	}
	public void setCustomer_phone(String customer_phone) {
		this.customer_phone = customer_phone;
	}
	public String getArea_num() {
		return area_num;
	}
	public void setArea_num(String area_num) {
		this.area_num = area_num;
	}
	public String getPackage_code() {
		return package_code;
	}
	public void setPackage_code(String package_code) {
		this.package_code = package_code;
	}
	public String getCustomer_name() {
		return customer_name;
	}
	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}
	public String getCustomer_idcard() {
		return customer_idcard;
	}
	public void setCustomer_idcard(String customer_idcard) {
		this.customer_idcard = customer_idcard;
	}
	
}
